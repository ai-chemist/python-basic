# ** 제곱 연산

print (2 ** 3)

# python 에서 '/'
print (10 / 3)

# 부동 소수점 연산
print(0.1 + 0.2)

# 정수와 실수 연산
print(1 + 2 + 3.0)

# '_' 기호 사용 -> 코드 작성 시 가독성 향상, 실행 시 영향 없음
print(f"$ {1_000_000_000_000}")

# 다중 할당 -> 한 행에서 여러 변수에 값 할당 가능
a, b, c, d = 4, 3, 2, 1
print(a, b, c, d)

# 상수 -> 문법적 지원 X, 관례 상 대문자 작성
CONSTANT = 99999
print(CONSTANT)