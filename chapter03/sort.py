alphabets = ['bbb', 'ccc', 'aaa']

# sort() - 알파벳 순으로 정렬
alphabets.sort()
print(alphabets)

# reverse=True 속성을 넣어 역순으로 정렬
alphabets.sort(reverse=True)
print(alphabets)

# sorted(리스트) - 임시 정렬
print(sorted(alphabets))
print(alphabets)

# reverse() 로 역순 정렬
alphabets.reverse()
print(alphabets)

# len() 리스트의 길이 반환
print(len(alphabets))